package cu.edu.cujae.utils;

public class ImportsAux {
	
	private String month;
	private String year;
	private float year$;
	private float month$;
	
	
	
	public ImportsAux(String month, String year, float year$, float month$) {
		this.month = month;
		this.year = year;
		this.year$ = year$;
		this.month$ = month$;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public float getYear$() {
		return year$;
	}
	public void setYear$(float year$) {
		this.year$ = year$;
	}
	public float getMonth$() {
		return month$;
	}
	public void setMonth$(float month$) {
		this.month$ = month$;
	}
	
	

}
